package bochassportclub.bsc.Entities;

public class Jugador {
    private int IdPersona;
    private int IdTipoJugador;

    public Jugador() {
    }

    public int getIdPersona() {
        return IdPersona;
    }

    public void setIdPersona(int idPersona) {
        IdPersona = idPersona;
    }

    public int getIdTipoJugador() {
        return IdTipoJugador;
    }

    public void setIdTipoJugador(int idTipoJugador) {
        IdTipoJugador = idTipoJugador;
    }
}
